// Helper function to get routes for a specific year
export const getRoutesByYear = (year) => {
  return galleryRoutes.filter(route => route.path.startsWith(`/${year}/`))
}

// Helper function to get all years
export const getAllYears = () => {
  const years = [...new Set(galleryRoutes.map(route => route.path.split('/')[1]))]
  return years.sort((a, b) => b - a) // Sort years in descending order
}
